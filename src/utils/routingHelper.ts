export const handleSmartClick = (
  e: React.MouseEvent,
  params: RouteParams,
  defaultAction: () => void
) => {
  // Middle click or Ctrl+Click â†’ New tab
  if (e.button === 1 || (e.button === 0 && (e.ctrlKey || e.metaKey))) {
    e.preventDefault();
    openInNewTab(params);
    return;
  }

  // Shift+Click â†’ New window
  if (e.button === 0 && e.shiftKey) {
    e.preventDefault();
    openInNewWindow(params);
    return;
  }

  // Normal click â†’ Default action
  if (e.button === 0) {
    defaultAction();
  }
};

